u8 mul(u8 a, u8 b)
{
	u8 r = 0;
	u8 tmp = b;
	u32 i;
	for (i = 0; i < 8; i++)
	{
		if (a & 1)
			r ^= tmp;
		tmp = MUL2(tmp);

		a >>= 1;
	}

	return r;
}
